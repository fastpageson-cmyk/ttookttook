// 정답 1개 = 5점, 100점 만점
export function scoreDiagnosis(answers) {
  return DIAGNOSIS_QUESTIONS.reduce(
    (score, q, i) => score + (answers[i] === q.answer ? 5 : 0), 0)
}

// 카테고리별 정답/문항 수 — 진단 결과의 강·약 영역 표시 및 추후 수준별 커리큘럼 분기용
export function categoryBreakdown(answers) {
  const map = new Map()
  DIAGNOSIS_QUESTIONS.forEach((q, i) => {
    if (!map.has(q.category)) map.set(q.category, { category: q.category, correct: 0, total: 0 })
    const c = map.get(q.category)
    c.total += 1
    if (answers[i] === q.answer) c.correct += 1
  })
  return [...map.values()]
}
